/*
Initialize the Packages
*/
class VariablesIntro
{
	public static void main(String[] args)
	{
		int a;  // declare a Variable
		a = 10; // Initialize the value
		// Print the value
		System.out.println("\nVariable a value is: "+ a); // (+) Symbol is Correlation 
		int b = 20; // Declare & Initialise the value
		// Print the value
		System.out.println("Variable b value is: "+b); // (+) Symbol is Correlation 
		int c = a; // The Variable a value to store variable c
		// print the value c
		System.out.println("Variable c value is: "+c); // (+) Symbol is Correlation 
	} // End of main method
} // End of VariablesIntro Class